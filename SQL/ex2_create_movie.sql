CREATE TABLE `Movie` (
  `movieId` varchar(30) ,
  `movieName` varchar(50) ,
  `movieDescription` varchar(500) ,
  `movieDirector` varchar(30) ,
  `movieRating` varchar(30) ,
  `mainActor` varchar(30),
  `duration` varchar(30) ,
  `category` varchar(30) ,
  `genre` varchar(30) ,
  PRIMARY KEY (`movieId`)
)